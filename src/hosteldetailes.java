        // TODO add your handling code here:
        
        String name=jTextField1.getText();
        String hid=jTextField2.getText();
        String address=jTextField3.getText();
        String numberofroom=jTextField4.getText();
        String type=(String)jComboBox1.getSelectedItem();
        String fees=jTextField5.getText();
        String test="0";
        
  
        try
        {
            Connection con=ConnectionProvider.getCon();
            PreparedStatement ps=con.prepareStatement("insert into hosteldetailes values(?,?,?,?,?,?,?)");
            ps.setString(1, name);
            ps.setString(2,hid);
            ps.setString(3,address);
            ps.setString(4,numberofroom);
            ps.setString(5,type);
            ps.setString(6,fees);
            ps.setString(7,test);


            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Sucessfully added");
            tableDetailes();
            clear();
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
