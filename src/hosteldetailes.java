      public void tableDetailes()
    {
        DefaultTableModel dtm=(DefaultTableModel)jTable1.getModel();
        dtm.setRowCount(0);
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from hosteldetailes");
            while(rs.next())
            {
                dtm.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});
                
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
            
        }
    }
      public void verify()
      {
          String hid=jTextField8.getText();
          int i=0;
        try
        
        {           
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select r.hid,r.activate,h.test from hos r,hosteldetailes h where r.hid='"+hid+"' and r.activate is not null and h.test <>0");
           
            while(rs.next())
            {
               i=1;
                JOptionPane.showMessageDialog(null,"This hostel cannot be updated or deleted,hostelrooms has been alloted");
                jButton3.setEnabled(false);
                jButton4.setEnabled(false);
                jButton4.setEnabled(false);
                jTextField6.setEditable(false);
                jTextField7.setEditable(false);
                clear();
               break;
               
              }
                  
                  if(i==0)
                      
                  {
                      jTextField6.setEditable(true);
                      jTextField7.setEditable(true);
                      jButton3.setEnabled(true);
                      jButton4.setEnabled(true);
                     
                  }
                   
            
        }
        catch(Exception e)  
        {
            JOptionPane.showMessageDialog(null, e);
        }
          
      }
      
      public void search()
      {
          String hid=jTextField8.getText();
          int i=0;
          try
          {
          Connection con=ConnectionProvider.getCon();
          Statement st=con.createStatement();
          ResultSet rs=st.executeQuery("select * from hosteldetailes where Hid='"+hid+"'");
          while(rs.next())
         {
             i=1;
              if(rs.getString(2).equals(hid))
              {
                  jTextField8.setBackground(Color.green);
                  verify();
                  
              }
                     
         }
          
          if(i==0)
          {    jTextField8.setBackground(Color.pink);
                  
                  jTextField6.setEditable(false);
                  jTextField7.setEditable(false);
              JOptionPane.showMessageDialog(null,"Hostel Not Found");
                   clear();
          }
          
      }
          catch(Exception e)
          {
              JOptionPane.showMessageDialog(null, e);
          }
      }

